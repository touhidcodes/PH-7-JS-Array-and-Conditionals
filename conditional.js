var myMoney = 500;
var phonePrice = 5000;
// For one condition use if
if (myMoney > phonePrice) {
	// if condition is true this code block will execute
	console.log("I will buy phone");
}

// For two condition use if and else
if (myMoney > phonePrice) {
	// if condition is true this code block will execute
	console.log("I will buy phone");
} else {
	// if condition is false this code block will execute
	console.log("I won't buy phone");
}
